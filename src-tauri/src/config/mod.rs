impl ConfigFile {
    pub fn new() -> Result<Self, ConfigError> {
        let s = Config::builder()
            // Start off by merging in the "default" configuration file
            .add_source(
                File::with_name("config/default")
                .format(config::FileFormat::Yaml)
                .required(false))
            .add_source(Environment::with_prefix("stipant"))
            .build()?;

        // You can deserialize (and thus freeze) the entire configuration as
        s.try_deserialize()
    }
}

pub struct AppConfig {
    pub resource_encryption_key: Vec<u8>,
    pub dec_table: Vec<u8>,
    pub ref_table: Vec<u8>,
    pub decrypted_extensions: Vec<String>
}

impl AppConfig {
    pub fn new() -> Result<Self, anyhow::Error> {
        match ConfigFile::new() {
            Ok(config) => {
                Ok(Self {
                    resource_encryption_key: general_purpose::STANDARD.decode(config.resource_encode_key)?,
                    dec_table: general_purpose::STANDARD.decode(config.dec_table)?,
                    ref_table: general_purpose::STANDARD.decode(config.ref_table)?,
                    decrypted_extensions: config.decrypted_extensions.split(';').map(str::to_string).collect()
                })
            },
            Err(_) => Ok(AppConfig::default())
        }
    }
}

